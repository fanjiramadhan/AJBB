                    <p>Ciptakan Kesejahteraan Secara Mandiri</p>

<P>orang mendambakan kesejahteraan untuk dirinya dan keluarga yang dicintainya. Namun, keterbatasan penghasilan menjadi kendala untuk mewujudkan kesejahteraan yang diidamkan. Bahkan, tak jarang kesejahteraan menjadi sesuatu yang jauh dari jangkauan. Salah satu cara mewujudkan kesejahteraan tanpa harus menunggu penghasilan lebih adalah program Mitra (Bumiputera) Proteksi Mandiri.
Mitra Proteksi Mandiri menawarkan solusi bagi Anda yang ingin menciptakan sendiri kesejahteraan keluarga tanpa mengharapkan kontribusi pihak lain.
Dengan memiliki Mitra Proteksi Mandiri, Anda telah melakukan langkah awal yang tepat untuk mendapatkan kepastian kesejahteraan keluarga yang akan terus meningkat dari tahun ke tahun, sekaligus mendapatkan manfaat tabungan.</P>
<P>Mitra Proteksi Mandiri diperuntukkan bagi Anda untuk merencanakan kesejahteraan masa pensiun. Mitra Proteksi Mandiri adalah program asuransi mikro dari Bumiputera untuk Anda yang berprofesi sebagai petani, nelayan, peternak atau pekerja lainnya. Meskipun berpenghasilan terbatas, Anda tetap bisa menikmati masa pensiun dengan tenang layaknya seorang pegawai.
Manfaat </P>
<p>1. Jika Tertanggung hidup hingga akhir masa asuransi, maka pemegang polis akan menerima Nilai Tunai pada saat habis kontrak.

<p>2. Apabila Tertanggung meninggal dunia dalam masa asuransi, maka ahli waris sebagai pihak yang ditunjuk akan menerima santunan meninggal sebesar :
• Uang Pertanggungan yang meningkat pada saat meninggal
• Nilai tunai pada saat meninggal   
Selanjutnya asuransi berakhir.</p>
<P>3. Jika Pemegang Polis mengundurkan diri, maka ia akan menerima Nilai Tunai pada saat mengundurkan diri dan selanjutnya asuransi berakhir.

Keunggulan

• Bebas biaya polis
• Premi minimal hanya Rp.200.000,- per bulan / polis
• Uang Pertanggungan pada awal asuransi sebesar 24 kali premi bulanan.
• Uang Pertanggungan Mitra Proteksi Mandiri terus meningkat sebesar 10% dari Uang Pertanggungan awal setiap tahun mulai tahun ke dua dan seterusnya hingga akhir kontrak atau jika tertanggung meninggal dunia.</p>

