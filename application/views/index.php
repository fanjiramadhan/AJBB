                    <p>Setiap orang tua menginginkan masa depan yang cerah bagi anak-anak mereka, dan memberikan mereka awal yang lebih baik. 
                        Namun banyak orang tua kuatir bahwa melonjaknya biaya sekolah akan menempatkan pendidikan yang baik di luar jangkauan anak-anak mereka, dan kuatir akan masa depan anak-anaknya jika terjadi sesuatu terhadap mereka.
                    
Mitra Beasiswa disediakan dalam mata uang Rupiah dan merupakan program Mitra Beasiswa yang menjamin pembiayaan pendidikan 
anak sepenuhnya, mulai dari taman kanak-kanak hingga perguruan tinggi, terlepas dari perubahan keadaan keuangan.
Mitra Beasiswa dirancang khusus untuk menjadi mitra anak dalam pendidikan, memastikan anak-anak Anda secara teratur
 mendapatkan uang yang mereka butuhkan untuk melanjutkan pendidikan mereka. Masa depan anak-anak juga terlidungi karena program ini dirancang untuk memastikan agar mereka tetap mendapatkan dana beasiswa hingga mereka lulus, walaupun jika orang tua mereka meninggal dunia.
Beragam Manfaat
Melalui Mitra Beasiswa, manfaat yang Anda akan dapatkan meliputi:

<P>a Dana Kelangsungan Belajar (DKB) yang dibayarkan secara bertahap, sesuai dengan tingkat usia anak, baik Tertanggung hidup atau meninggal dunia.
<p>b. Dana Beasiswa anak, dibayarkan pada saat periode asuransi berakhir, baik tertanggung masih hidup atau meninggal dunia.
<p>c. Santunan meninggal dunia sebesar 100% dari uang pertanggungan.
<p>d. Bebas premi bagi polis jika Tertanggung meninggal dunia.
<p>e. Pengembalian simpanan premi bagi polis saat Tertanggung meninggal dunia jia premi dibayarkan secara penuh setelah jumlah premi diperhitungkan
<p>f. Hak untuk mendapatkan Reversionary Bonus, jika Tertanggung meninggal dunia, penebusan polis, atau habis kontrak.

<P>Persyratan
Jika Anda berusia 21 tahun atau sudah menikah, Anda berhak untuk menjadi seorang pemegang polis. Pemegang polis belum tentu termasuk Tertanggung, tetapi antara Pemegang Polis dan Tertanggung, hubungan kepentingan dalam asuransi - suami, istri dan anak yang sah.
Masa pertanggungan untuk asuransi ini adalah minimum 2 tahun dan maksimum 17 tahun. Premi dibayar dalam mata uang Rupiah, dan dapat dibayar sekaligus, tahunan, setengah tahunanan, atau tiap tiga bulan.
Asuransi dapat diperoleh dengan atau tanpa pemeriksaan kesehatan. Masa observasi untuk kewajiban tanpa pemeriksaan kesehatan adalah 2 tahun, kecuali jika terjadi kecelakaan atau adanya wabah penyakit yang telah dinyatakan oleh lembaga kesehatan setempat, di mana ketentuan untuk masa observasi tanpa pemeriksaan kesehatan tidak berlaku.</p>
